export type OutputFormat = "jpeg" | "png" | "webp" | "avif";
export type FormatPreference = "auto" | "original" | OutputFormat;
export type CompressionStrategy =
  | "same-format"
  | "png-optimize"
  | "png-quantized"
  | "png-encode-fallback"
  | "webp-lossless"
  | "webp-lossy"
  | "avif-lossy";

export type PngMode = "lossless" | "compressed";

export interface CompressionSettings {
  format: FormatPreference;
  lossless: boolean;
  pngColors: number;
  pngMode: PngMode;
  quality: number;
}

export interface CompressionPreset {
  description: string;
  id: string;
  label: string;
  settings: CompressionSettings;
}

export type CompressionVariantStatus =
  | "processing"
  | "done"
  | "larger-than-original"
  | "error";

export type CompressionJobStatus = "queued" | "processing" | "done" | "error";

export interface CompressionVariant {
  // True when produced by Auto mode (a Safe + Smaller candidate pair),
  // as opposed to a manual format choice or a preset. Drives whether the
  // app auto-selects the quality-safe pick instead of the absolute smallest.
  auto: boolean;
  autoGenerated: boolean;
  createdAt: number;
  error?: string;
  format: OutputFormat;
  id: string;
  note?: string;
  output?: Blob;
  outputName?: string;
  presetId: string | null;
  progress: number;
  settings: CompressionSettings;
  signature: string;
  sizeDelta: number | null;
  sourceFormat: OutputFormat | null;
  status: CompressionVariantStatus;
  strategy: CompressionStrategy;
}

export interface CompressionJob {
  activeVariantId: string | null;
  bestVariantId: string | null;
  error?: string;
  file: File;
  id: string;
  status: CompressionJobStatus;
  variants: CompressionVariant[];
}

export interface WorkerCompressRequest {
  autoGenerated: boolean;
  file: File;
  presetId: string | null;
  settings: CompressionSettings;
  sourceFormat: OutputFormat | null;
  sourceId: string;
  strategy: CompressionStrategy;
  variantId: string;
}

export type WorkerProgressStage = "decoding" | "encoding";

export interface WorkerCompressProgress {
  kind: "progress";
  progress: number;
  sourceId: string;
  stage: WorkerProgressStage;
  variantId: string;
}

export interface WorkerCompressSuccess {
  autoGenerated: boolean;
  format: OutputFormat;
  kind: "result";
  note?: string;
  ok: true;
  output: Blob;
  outputName: string;
  sourceFormat: OutputFormat | null;
  sourceId: string;
  strategy: CompressionStrategy;
  variantId: string;
}

export interface WorkerCompressError {
  error: string;
  kind: "result";
  ok: false;
  sourceId: string;
  variantId: string;
}

export interface WorkerRuntimeIssue {
  detail?: string;
  error: string;
  kind: "runtime-error";
  sourceId?: string;
  variantId?: string;
}

export type WorkerCompressResponse =
  | WorkerCompressProgress
  | WorkerCompressSuccess
  | WorkerCompressError
  | WorkerRuntimeIssue;
